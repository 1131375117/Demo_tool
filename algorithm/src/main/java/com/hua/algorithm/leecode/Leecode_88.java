class Solution {
    public void merge(int[] nums1, int m, int[] nums2, int n) {
        int k = m + n;
        for (int index = k - 1, num1Index = m - 1, num2Index = n - 1;
             index >= 0; index--) {
            //首先，如果nums1使用完
            if (num1Index < 0) {
                nums1[index] = nums2[num2Index--];
            } else if (num2Index < 0) {
                break;
            } else if (nums1[num1Index] > nums2[num2Index]) {
                nums1[index] = nums1[num1Index];
            } else {
                nums1[index] = nums2[num2Index];
            }
        }
    }
}
